# Convert the temperature from Celius to Fahrenhiet 

Celius = float(input("Enter temperature in Celius:"))
Fahrenhiet = (Celius * 9/5) + 32
print(f"Temperature in Fahrenhiet is:{Fahrenhiet}")

# Here we are taking temperature in Celius as input from user and then converting it to Fahrenhiet using the formula (Celius * 9/5) + 32. 
# Finally, we are printing the result on the screen.
# here f-string is used to format the output string. f-strings are a way to embed expressions inside string literals, using curly braces {}.
