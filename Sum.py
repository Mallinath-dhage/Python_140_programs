#Write a Program to input 2 numbers & print their sum.
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))
sum = num1 + num2
print(f"The sum of", {num1}, "and", {num2}, "is:", {sum})

# Here we are taking two numbers as input from the user using the input() function and converting them to float type.
# We then calculate their sum using the + operator and store it in the variable 'sum'.
# Finally, we print the result using an f-string to format the output.  